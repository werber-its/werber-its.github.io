const getSubNav = function (subNavName) {
  let newMenuData;
  newMenuData = menuData.find((menuItem) => menuItem.wpName === subNavName);
  if (!newMenuData) {
    newMenuData = menuData.find(
      (menuItem) => menuItem.wpParentName === subNavName
    );
    console.log("newMenuData", newMenuData);
    console.log("nah");
  }
  if (newMenuData) {
    console.log("newMenuData", newMenuData);
    const subNav = document.getElementById("nkba-icon-sub-nav");

    if (newMenuData || subNavName === "home") {
      while (subNav.firstChild) {
        subNav.removeChild(subNav.firstChild);
      }
    }

    for (let i = 0; i < newMenuData.subNavCategories.length; i++) {
      const el = document.createElement("a");
      el.classList.add("nkba-icon-sub-nav-item");
      el.href = newMenuData.subNavCategories[i].url;
      el.innerText = newMenuData.subNavCategories[i].title;
      subNav.appendChild(el);
    }
  }
};

